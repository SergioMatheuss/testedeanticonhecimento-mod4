// Encontrar o dobro de um número caso ele seja positivo e o seu triplo caso seja negativo, 
// imprimindo o resultado. 

let pos = 10 ;
let neg = -4;

if (pos > 0 && pos*2) {
    document.write(`${pos * 2} é positivo <br>`);
    
    if (neg < 0 && neg*3) {
        document.write(`${neg * 3} é negativo <br>`);
        
    }
};